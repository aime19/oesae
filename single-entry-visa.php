                                    <p><strong>DIRECTORATE GENERAL OF IMMIGRATION AND EMIGRATION ANNOUNCE A NEW VISA REGIME </strong></p>
                                    <p><strong> <i>May 1, 2018</i>  </strong></p>
                                    <p>Reference to Cabinet meeting of <strong>08.11.2017</strong> that approved the establishment
                                        of new visa regime for the Republic of Rwanda; the Directorate General of Immigration
                                        and Emigration would like to inform the public the following New Visa Regime:</p>
                                    <ol class="spip">
                                        <li> <strong>Reciprocity </strong> - Rwanda will grant visa free of charge with 90 days
                                            validity to the following countries on reciprocal basis: Benin, Central African
                                            Republic, Chad, Ghana, Guinea, Indonesia, Haiti, Senegal, Seychelles and Sao
                                            Tome and Principe. This is in addition to Democratic Republic of Congo, East
                                            African Member Community Partner States, Mauritius, Philippines, and Singapore.
                                            This takes immediate effect.</li><br>
                                        <li> Visa waiver for diplomatic and service passports - The Government of Rwanda has entered
                                            into Visa Waiver Agreement for holders of Diplomatic and Service Passports with;
                                            Djibouti, Ethiopia, Gabon, Guinea, India, Israel, Morocco and Turkey. This takes
                                            immediate effect.</li><br>
                                        <li> <strong>30 days visa upon arrival</strong> — Citizens of all countries to get visa
                                            upon arrival without prior application, starting 01 January 2018. Before that,
                                            only nationals of African countries and few others were getting visa upon arrival.</li><br>
                                        <li>
                                            <strong>COMESA Member States to get 90 days visa upon arrival</strong> - Citizens
                                            of COMESA member states to get 90 days visa on arrival (on payment of the prescribed
                                            fee) as provided by Article 4 of the COMESA Protocol on Free Movement of Persons,
                                            Labour, Services, Right of Establishment and Residence. Rwanda ratified the protocol
                                            on 13th July 2016. Currently, citizens of COMESA member states were getting 30
                                            days on arrival as other holders of African Passports.</li><br>
                                        <li> <strong>Rwandans living abroad with dual nationality allowed to use national IDs on entry</strong>                                            - waive visa fee to Rwandans travelling on foreign passports in possession of
                                            a Rwandan Identity Card for countries that permit dual nationality only. Initially
                                            Rwandans travelling on a foreign passport in possession of a valid Rwandan passport
                                            were the only granted visa free on arrival.</li><br>
                                        <li> <strong>Foreign residents to use their Resident ID cards for entry</strong> - returning
                                            residents holding valid residence permits with corresponding resident cards will
                                            be allowed to use them on entry, includingusing electronic gates (Automated Passenger
                                            Clearance System) at Kigali international Airport.</li><br>
                                    </ol>
